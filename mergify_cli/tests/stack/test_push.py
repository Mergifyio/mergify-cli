@pytest.mark.respx(base_url="https://api.github.com/")
async def test_stack_update_no_rebase(
    git_mock: test_utils.GitMock,
    respx_mock: respx.MockRouter,
) -> None:
    # Mock 1 commits on branch `current-branch`
    git_mock.commit(
        test_utils.Commit(
            sha="commit_sha",
            title="Title",
            message="Message",
            change_id="I29617d37762fd69809c255d7e7073cb11f8fbf50",
            head_ref="current-branch/I29617d37762fd69809c255d7e7073cb11f8fbf50",
        ),
    )
    git_mock.finalize(
        remote_shas={
            "I29617d37762fd69809c255d7e7073cb11f8fbf50": "previous_commit_sha",
        },
    )

    # Mock HTTP calls: the stack already exists but it's out of date, it should
    # be updated
    respx_mock.get("/user").respond(200, json={"login": "author"})
    respx_mock.get("/search/issues").respond(
        200,
        json={
            "items": [
                {
                    "pull_request": {
                        "url": "https://api.github.com/repos/user/repo/pulls/123",
                    },
                },
            ],
        },
    )

    respx_mock.get(
        "/repos/user/repo/pulls/123",
    ).respond(
        200,
        json={
            "html_url": "",
            "number": "123",
            "title": "Title",
            "head": {
                "sha": "previous_commit_sha",
                "ref": "current-branch/I29617d37762fd69809c255d7e7073cb11f8fbf50",
            },
            "body": "body",
            "state": "open",
            "merged_at": None,
            "draft": False,
            "node_id": "",
        },
    )
    patch_pull_mock = respx_mock.patch("/repos/user/repo/pulls/123").respond(
        200,
        json={},
    )
    respx_mock.get("/repos/user/repo/issues/123/comments").respond(
        200,
        json=[
            {
                "body": "This pull request is part of a stack:\n...",
                "url": "https://api.github.com/repos/user/repo/issues/comments/456",
            },
        ],
    )
    patch_comment_mock = respx_mock.patch(
        "/repos/user/repo/issues/comments/456",
    ).respond(200)
    respx_mock.post("/repos/user/repo/issues/123/comments").respond(200)
    git_mock.mock("fetch", "origin", "refs/pull/123/head", output="")

    with (
        mock.patch.object(push, "detect_change_type", return_value="content"),
        mock.patch.object(replay, "replay_for_revision", return_value=None),
    ):
        await push.stack_push(
            github_server="https://api.github.com/",
            token="",
            skip_rebase=True,
            next_only=False,
            branch_prefix="",
            dry_run=False,
            trunk=("origin", "main"),
        )
    assert not git_mock.has_been_called_with("pull", "--rebase", "origin", "main")

    # The pull request is updated
    assert len(patch_pull_mock.calls) == 1
    assert json.loads(patch_pull_mock.calls.last.request.content) == {
        "head": "current-branch/I29617d37762fd69809c255d7e7073cb11f8fbf50",
        "base": "main",
        "title": "Title",
        "body": "Message",
    }

    # Old-format stack comment is updated to new table format
    assert len(patch_comment_mock.calls) == 1
    comment_body = json.loads(patch_comment_mock.calls.last.request.content)["body"]
    assert comment_body.startswith(
        "This pull request is part of a [Mergify stack]",
    )


