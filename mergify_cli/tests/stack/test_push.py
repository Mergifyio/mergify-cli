def _make_local_change(
    *,
    change_id: str,
    pull_number: int,
    head_sha: str,
    base_branch: str,
    dest_branch: str,
    title: str = "t",
) -> tuple[changes.LocalChange, github_types.PullRequest]:
    pull = typing.cast(
        "github_types.PullRequest",
        {
            "number": pull_number,
            "title": title,
            "html_url": f"https://github.com/owner/repo/pull/{pull_number}",
            "head": {"ref": dest_branch, "sha": head_sha},
            "base": {"ref": base_branch},
            "draft": False,
            "merged_at": None,
            "merge_commit_sha": None,
            "body": "",
            "state": "open",
        },
    )
    change = changes.LocalChange(
        id=changes.ChangeId(change_id),
        pull=pull,
        commit_sha=head_sha,
        title=title,
        message="",
        base_branch=base_branch,
        dest_branch=dest_branch,
        action="update",
    )
    return change, pull


